package br.com.dfdevforge.common.services;

import java.util.Map;

import br.com.dfdevforge.common.exceptions.BaseException;

public interface CommonService {
	default Map<String, Object> execute() throws BaseException {
		this.validateUserAccess();
		this.executeBusinessRule();
		this.configureUserActions();

		return this.returnBusinessData();
	}

	/**
	 * Its purpose is to validate user access before executing the business rule.
	 */
	public void validateUserAccess() throws BaseException;

	/**
	 * Its purpose is to group all necessary methods to business rule execution.
	 * @throws BaseException 
	 */
	public void executeBusinessRule() throws BaseException;

	/**
	 * Its purpose is to group all necessary methods to configure the visibility of user components.
	 */
	public void configureUserActions();

	/**
	 * Its purpose is to group all content generated by the business rule and return it to the controller layer.
	 * 
	 * @return Map<String, Object>
	 */
	public Map<String, Object> returnBusinessData();

}
